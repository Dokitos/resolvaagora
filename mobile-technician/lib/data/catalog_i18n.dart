bool _en(Locale l) => l.languageCode == 'en';

extension CategoryI18n on ServiceCategory {
  String localizedName(Locale l) => _en(l) ? (_nameEn[id] ?? name) : name;
  String localizedDescription(Locale l) => _en(l) ? (_descEn[id] ?? description) : description;
}

extension SubcategoryI18n on ServiceSubcategory {
  String localizedName(Locale l) => _en(l) ? (_nameEn[id] ?? name) : name;
  String localizedDescription(Locale l) => _en(l) ? (_descEn[id] ?? description) : description;
}

extension ItemI18n on ServiceItem {
  String localizedName(Locale l) => _en(l) ? (_nameEn[id] ?? name) : name;
}
